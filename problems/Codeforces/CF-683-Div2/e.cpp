int solve(vi curr, int bit) {
    if (bit < -1) return 0;
    if (sz(curr) == 1) return 1;
    vi a, b;
    for (auto x : curr) {
        if (x & (1 << bit)) {
            a.pb(x);
        } else {
            b.pb(x);
        }
    }
    if (!sz(b)) return solve(a, bit - 1);
    if (!sz(a)) return solve(b, bit - 1);
    return 1 + max(solve(a, bit - 1), solve(b, bit - 1));
}

